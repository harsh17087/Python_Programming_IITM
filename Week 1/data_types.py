# Identifying the type of different data types

a = 10
b = 10.0
c = True
d = 'Hello'
e = "Python is saying hello"
l = [10, 20, 30, 40, 50]
s = l[3]

print("The type of a is ", type(a))
print("The type of b is ", type(b))
print("The type of c is ", type(c))
print("The type of d is ", type(d))
print("The type of e is ", type(e))
print("The type of l is ", type(l))
print("The type of s is ", type(s))


# The output of the above program is
'''
The type of a is  <class 'int'>
The type of b is  <class 'float'>
The type of c is  <class 'bool'>
The type of d is  <class 'str'>
The type of e is  <class 'str'>
The type of l is  <class 'list'>
The type of s is  <class 'int'>

'''